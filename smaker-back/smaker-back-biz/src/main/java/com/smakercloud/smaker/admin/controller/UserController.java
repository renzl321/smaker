package com.smakercloud.smaker.admin.controller;

import com.baomidou.mybatisplus.core.conditions.query.QueryWrapper;
import com.baomidou.mybatisplus.core.toolkit.Wrappers;
import com.baomidou.mybatisplus.extension.plugins.pagination.Page;
import com.smakercloud.smaker.admin.api.dto.UserDTO;
import com.smakercloud.smaker.admin.api.entity.SysUser;
import com.smakercloud.smaker.admin.service.SysUserService;
import com.smakercloud.smaker.common.core.util.SmakerResult;
import com.smakercloud.smaker.common.log.annotation.SysLog;
import com.smakercloud.smaker.common.security.annotation.Inner;
import com.smakercloud.smaker.common.security.util.SecurityUtils;
import lombok.AllArgsConstructor;
import org.springframework.security.access.prepost.PreAuthorize;
import org.springframework.web.bind.annotation.*;

import javax.validation.Valid;

/**
 * @author renzl
 * @date 2019/2/1
 */
@RestController
@AllArgsConstructor
@RequestMapping("/user")
public class UserController {
	private final SysUserService userService;

	/**
	 * 获取当前用户全部信息
	 *
	 * @return 用户信息
	 */
	@GetMapping(value = {"/info"})
	public SmakerResult info() {
		String username = SecurityUtils.getUser().getUsername();
		SysUser user = userService.getOne(Wrappers.<SysUser>query()
				.lambda().eq(SysUser::getUsername, username));
		if (user == null) {
			return new SmakerResult<>(Boolean.FALSE, "获取当前用户信息失败");
		}
		return new SmakerResult<>(userService.findUserInfo(user));
	}

	/**
	 * 获取指定用户全部信息
	 *
	 * @return 用户信息
	 */
	@Inner
	@GetMapping("/info/{username}")
	public SmakerResult info(@PathVariable String username) {
		SysUser user = userService.getOne(Wrappers.<SysUser>query()
				.lambda().eq(SysUser::getUsername, username));
		if (user == null) {
			return new SmakerResult<>(Boolean.FALSE, String.format("用户信息为空 %s", username));
		}
		return new SmakerResult<>(userService.findUserInfo(user));
	}

	/**
	 * 通过ID查询用户信息
	 *
	 * @param id ID
	 * @return 用户信息
	 */
	@GetMapping("/{id}")
	public SmakerResult user(@PathVariable Integer id) {
		return new SmakerResult<>(userService.selectUserVoById(id));
	}

	/**
	 * 根据用户名查询用户信息
	 *
	 * @param username 用户名
	 * @return
	 */
	@GetMapping("/details/{username}")
	public SmakerResult user(@PathVariable String username) {
		SysUser condition = new SysUser();
		condition.setUsername(username);
		return new SmakerResult<>(userService.getOne(new QueryWrapper<>(condition)));
	}

	/**
	 * 删除用户信息
	 *
	 * @param id ID
	 * @return R
	 */
	@SysLog("删除用户信息")
	@DeleteMapping("/{id}")
	@PreAuthorize("@pms.hasPermission('sys_user_del')")
	public SmakerResult userDel(@PathVariable Integer id) {
		SysUser sysUser = userService.getById(id);
		return new SmakerResult<>(userService.deleteUserById(sysUser));
	}

	/**
	 * 添加用户
	 *
	 * @param userDto 用户信息
	 * @return success/false
	 */
	@SysLog("添加用户")
	@PostMapping
	@PreAuthorize("@pms.hasPermission('sys_user_add')")
	public SmakerResult user(@RequestBody UserDTO userDto) {
		return new SmakerResult<>(userService.saveUser(userDto));
	}

	/**
	 * 更新用户信息
	 *
	 * @param userDto 用户信息
	 * @return R
	 */
	@SysLog("更新用户信息")
	@PutMapping
	@PreAuthorize("@pms.hasPermission('sys_user_edit')")
	public SmakerResult updateUser(@Valid @RequestBody UserDTO userDto) {
		return new SmakerResult<>(userService.updateUser(userDto));
	}

	/**
	 * 分页查询用户
	 *
	 * @param page    参数集
	 * @param userDTO 查询参数列表
	 * @return 用户集合
	 */
	@GetMapping("/page")
	public SmakerResult getUserPage(Page page, UserDTO userDTO) {
		return new SmakerResult<>(userService.getUsersWithRolePage(page, userDTO));
	}

	/**
	 * 修改个人信息
	 *
	 * @param userDto userDto
	 * @return success/false
	 */
	@SysLog("修改个人信息")
	@PutMapping("/edit")
	public SmakerResult updateUserInfo(@Valid @RequestBody UserDTO userDto) {
		return userService.updateUserInfo(userDto);
	}

	/**
	 * @param username 用户名称
	 * @return 上级部门用户列表
	 */
	@GetMapping("/ancestor/{username}")
	public SmakerResult listAncestorUsers(@PathVariable String username) {
		return new SmakerResult<>(userService.listAncestorUsers(username));
	}
}
